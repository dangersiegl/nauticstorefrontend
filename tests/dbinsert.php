$sql = [];
$categoryCounter = 1;
$subcategoryCounter = 1;
$subSubcategoryCounter = 1;

// Kategorien iterieren
foreach ($categories as $category) {
    $categoryId = $categoryCounter++;

    // INSERT für Hauptkategorie
    $sql[] = "INSERT INTO `products_categories` (`id`, `category`, `title_de`, `title_en`, `url_de`, `url_en`, `rownum`) VALUES 
              ($categoryId, 'Kategorie', '{$category['name']}', '{$category['name']}', '{$category['link']}', '{$category['link']}', $categoryId);";

    // Unterkategorien iterieren
    foreach ($category['subcategories'] as $subcategory) {
        $subcategoryId = $subcategoryCounter++;

        // INSERT für Unterkategorie
        $sql[] = "INSERT INTO `products_categories_subcategories` (`id`, `category_id`, `title_de`, `title_en`, `url_de`, `url_en`, `rownum`, `top_deal_id`) VALUES 
                  ($subcategoryId, $categoryId, '{$subcategory['name']}', '{$subcategory['name']}', '{$subcategory['link']}', '{$subcategory['link']}', $subcategoryId, NULL);";

        // Unter-Unterkategorien iterieren
        if (!empty($subcategory['subcategories'])) {
            foreach ($subcategory['subcategories'] as $subSubcategory) {
                $subSubcategoryId = $subSubcategoryCounter++;

                // INSERT für Unter-Unterkategorie
                $sql[] = "INSERT INTO `products_categories_sub_subcategories` (`id`, `subcategory_id`, `title_de`, `title_en`, `url_de`, `url_en`, `rownum`) VALUES 
                          ($subSubcategoryId, $subcategoryId, '{$subSubcategory['name']}', '{$subSubcategory['name']}', '{$subSubcategory['link']}', '{$subSubcategory['link']}', $subSubcategoryId);";
            }
        }
    }
}

// SQL-Befehle ausgeben
foreach ($sql as $query) {
    echo $query . "\n";
}

